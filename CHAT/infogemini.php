    <h1>1. Temperature:</h1>
    <h2>O que é:</h2>
    <p> Essa configuração determina a "criatividade" ou "aleatoriedade" das respostas do modelo Gemini.</p>
    <h3>Como funciona:</h3>
    <p>Valor baixo (próximo de 0): Respostas mais previsíveis, consistentes e baseadas em padrões. <br>
        Valor alto (próximo de 1): Respostas mais criativas, originais e imprevisíveis. Podem ser mais "aleatórias" e menos consistentes.</p>
    <h4>Exemplos:</h4>
    <p>Baixa temperatura: Ideal para tarefas que exigem respostas precisas e consistentes, como resumos de notícias ou respostas a perguntas factuais.
        <br>
        Alta temperatura: Ideal para tarefas que exigem criatividade, como escrever poemas, gerar ideias ou criar histórias.
    </p>
    <h4>Dicas:</h4>
    <p>Comece com uma temperatura média (0,5) e ajuste gradualmente para obter o resultado desejado. <br>
        Tenha em mente que uma temperatura alta pode resultar em respostas incoerentes ou sem sentido.</p>
    <h1>2. Tools:</h1>
    <h2>O que é:</h2>
    <p> Essa configuração permite você "ensinar" o modelo Gemini a usar ferramentas externas para completar tarefas. </p>
    <h3>Como funciona:</h3>
    <p>Você define quais ferramentas o modelo pode usar (ex: calculadora, busca na web, tradução).<br>
        O modelo pode acessar essas ferramentas e usar suas funcionalidades para gerar respostas mais completas.</p>
    <h4>Exemplos:</h4>
    <p>Calculadora: O modelo pode usar uma calculadora para realizar operações matemáticas. <br>
        Busca na web: O modelo pode procurar informações na internet para responder a perguntas complexas. <br>
        Tradução: O modelo pode usar uma ferramenta de tradução para gerar textos em diferentes idiomas.</p>
    <h4>Dicas:</h4>
    <p>Escolha as ferramentas que melhor se encaixam na tarefa que você quer que o modelo execute. <br>
        Forneça informações claras sobre como o modelo deve usar cada ferramenta. </p>
    <h1>3. Advanced settings:</h1>
    <h2>O que é:</h2>
    <p> Essa configuração contém opções avançadas para ajustar o comportamento do modelo Gemini. </p>
    <h3>Como funciona:</h3>
    <p>Max tokens: Define o número máximo de tokens que o modelo pode gerar em uma resposta. <br>
        Top_p: Controla a probabilidade de palavras serem selecionadas durante a geração de texto. <br>
        Top_k: Similar ao "top_p", mas limita o número de palavras consideradas na geração de texto. <br>
        Presença de penalidades: Penaliza a repetição de palavras ou frases no texto gerado. <br>
        Frequência de penalidades: Penaliza a geração de palavras ou frases que já apareceram com frequência no texto. </p>
    <h4>Exemplos:</h4>
    <p>Max tokens: Use para limitar o tamanho da resposta do modelo. <br>
        Top_p e top_k: Use para gerar textos mais diversificados e menos repetitivos. <br>
        Penalidades: Use para evitar que o modelo repita palavras ou frases excessivamente. </p>
    <h4>Dicas:</h4>
    <p>Explore cada opção e ajuste-as de acordo com suas necessidades. <br>
        Comece com as configurações padrão e altere-as gradualmente para observar o impacto. </p>
